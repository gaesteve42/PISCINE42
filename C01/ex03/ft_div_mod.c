#include <stdio.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int	main(void)
{
	int	a;
	int	b;
	int	div;
	int	mod;

	a = 254;
	b = 87;
	ft_div_mod(a, b, &div, &mod);
	printf("%d\n", a);
	printf("%d", b);
}
